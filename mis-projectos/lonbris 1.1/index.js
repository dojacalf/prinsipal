var algo = document.getElementById('algo');

function derecha() {
  return algo.style.transform = 'translateX(600px)';
}
function isquierda() {
  return algo.style.transform = 'translateX(0)';
}
function arriba() {
  return algo.style.transform = 'translateY(0)';
}
function abajo() {
  return algo.style.transform = 'translateY(250px)';
}
function morado() {
   algo.style.transform = 'translate(600px,250px)';
}
